class Appeal{
    constructor(appealID,appealTitle,appealTarget,fromDate,toDate,description,outcome,orgID){
        this.appealID = appealID;
        this.appealTitle = appealTitle;
        this.appealTarget = appealTarget;
        this.fromDate = fromDate;
        this.toDate = toDate;
        this.description = description;
        this.outcome = outcome;
        this.orgID = orgID;
    }
}